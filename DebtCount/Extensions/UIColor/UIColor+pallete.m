#import "UIColor+pallete.h"

@implementation UIColor (Pallete)

+ (UIColor *)darkViolet {
    return [UIColor colorWithRed:0.22 green:0.26 blue:0.35 alpha:1.0];
}

+ (UIColor *)fancyRed {
    return [UIColor colorWithRed:0.97 green:0.22 blue:0.35 alpha:1.0];
}

+ (UIColor *)lightCyan {
    return [UIColor colorWithRed:0.48 green:0.78 blue:0.77 alpha:1.0];
}

+ (UIColor *)iceBlue {
    return [UIColor colorWithRed:0.96 green:0.96 blue:0.96 alpha:1.0];
}

+ (UIColor *)justRed {
    return [UIColor redColor];
}

+ (UIColor *)justBlue {
    return [UIColor blueColor];
}

@end
